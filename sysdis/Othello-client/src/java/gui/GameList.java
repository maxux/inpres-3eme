    public void RefreshList() {
        parties = communication.ListeParties();
        ids.removeAll(ids);
        
        if(!parties.isEmpty()) {
            UpdateList(parties);
        }
    }
    
    public void UpdateList(List<Parties> parties) {
        DefaultListModel model = new DefaultListModel();
        
        if(!parties.isEmpty())
            jList1.removeAll();    
        
        for(Parties partie : parties) {
            ids.add(partie.id.intValue());
            model.addElement(partie.name);
        }
        
        if(parties.isEmpty())
            model.addElement("No games found");
       
        jList1.setModel(model);
    }

